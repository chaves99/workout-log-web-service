CREATE TABLE IF NOT EXISTS public.USER_WL (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT,
    password TEXT,
    email TEXT,
    UNIQUE(email)
);

CREATE TABLE IF NOT EXISTS public.WORKOUT (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    description TEXT,
    start TIMESTAMP,
    ending TIMESTAMP,
    user_id BIGINT REFERENCES public.USER_WL(id)
);

CREATE TABLE IF NOT EXISTS public.EXERCISE (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT,
    e_order INTEGER,
    workout_id BIGINT REFERENCES public.WORKOUT(id)
);

CREATE TABLE IF NOT EXISTS public.EXECUTION (
    name TEXT,
    e_order INTEGER,
    exercise_id BIGINT REFERENCES public.EXERCISE(id),
    PRIMARY KEY(exercise_id, e_order)
);